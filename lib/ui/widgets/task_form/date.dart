abstract class Date {
  static final Map<int, String> _monthList = {
    1: 'января',
    2: 'февраля',
    3: 'марта',
    4: 'апреля',
    5: 'мая',
    6: 'июня',
    7: 'июля',
    8: 'августа',
    9: 'сентября',
    10: 'октября',
    11: 'ноября',
    12: 'декабря',
  };
  static String getMonth(int month) {
    return _monthList[month] ?? '';
  }
}
